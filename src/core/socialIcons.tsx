// Display label for each network. Used as the icon link's
// `aria-label` and `title`. Proper nouns — same labels everywhere,
// not translated.
const ICON_LABELS: Record<SocialNetwork, string> = {
  twitter: "Twitter / X",
  linkedin: "LinkedIn",
  instagram: "Instagram",
  mastodon: "Mastodon",
  bluesky: "Bluesky",
  github: "GitHub",
  website: "Website",
};

interface SocialIconProps {
  network: SocialNetwork;
  className?: string;
  size?: number;
}

// React-side renderer used by AuthorTemplate. Returns an `<svg>` with
// the right path. Sized 18px by default — matches the mockup's
// `text-[18px]` Material Symbols.
export function SocialIcon({ network, className, size = 18 }: SocialIconProps) {
  const path = ICON_PATHS[network];
  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      viewBox="0 0 24 24"
      width={size}
      height={size}
      fill="currentColor"
      aria-hidden="true"
      className={className}
    >
      <path d={path} />
    </svg>
  );
}

export function socialLabel(network: SocialNetwork): string {
  return ICON_LABELS[network];
}

